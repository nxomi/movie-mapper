export const codeToCountryName = Object.entries(countryNameToCode).reduce((acc, [name, code]) => {
  if (!acc[code] || name.length > acc[code].length) {
    acc[code] = name;
  }
  return acc;
}, {});

codeToCountryName['USA'] = 'United States';
codeToCountryName['GBR'] = 'United Kingdom';
codeToCountryName['RUS'] = 'Russia';
codeToCountryName['KOR'] = 'South Korea';
codeToCountryName['DEU'] = 'Germany';
codeToCountryName['NLD'] = 'Netherlands';

export function normalizeCountryName(name) {
  if (!name) return null;
  const trimmed = name.trim();
  return countryNameToCode[trimmed] || null;
}

export function getDisplayName(code) {
  return codeToCountryName[code] || code;
}
